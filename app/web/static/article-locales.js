import { I18N_NATIVE_TAROT_EN_0006_ARTICLE_LOCALES } from "./article-locale-i18n-native-tarot-en-0006.js?v=i18n-native-tarot-en-0006";

import { I18N_NATIVE_TAROT_JA_0002_ARTICLE_LOCALES } from "./article-locale-i18n-native-tarot-ja-0002.js?v=i18n-native-tarot-ja-0002";

import { I18N_NATIVE_TAROT_KO_0002_ARTICLE_LOCALES } from "./article-locale-i18n-native-tarot-ko-0002.js?v=i18n-native-tarot-ko-0002";

export const ARTICLE_LOCALE_REGISTRY = [
  ...I18N_NATIVE_TAROT_EN_0006_ARTICLE_LOCALES,
  ...I18N_NATIVE_TAROT_JA_0002_ARTICLE_LOCALES,
  ...I18N_NATIVE_TAROT_KO_0002_ARTICLE_LOCALES,
];

export function listArticleLocaleRecords() {
  return ARTICLE_LOCALE_REGISTRY.map((record) => ({ ...record }));
}

export function parseArticleLocalePath(pathname = "") {
  const normalized = pathname.startsWith("/") ? pathname : `/${pathname}`;
  const match = normalized.match(/^\/(en|ja|ko)(\/|$)/);
  if (!match) {
    return { locale: DEFAULT_ARTICLE_LOCALE, pathname: normalized || "/" };
  }
  const stripped = normalized.slice(match[1].length + 1) || "/";
  return { locale: match[1], pathname: stripped.startsWith("/") ? stripped : `/${stripped}` };
}

export function getArticleLocaleRecord(articleId, locale) {
  if (!articleId || locale === DEFAULT_ARTICLE_LOCALE) return null;
  return ARTICLE_LOCALE_REGISTRY.find((record) => record.articleId === articleId && record.locale === locale) || null;
}

export function listPublishedArticleLocales(articleId) {
  const locales = ARTICLE_LOCALE_REGISTRY
    .filter((record) => record.articleId === articleId)
    .map((record) => record.locale);
  return [DEFAULT_ARTICLE_LOCALE, ...new Set(locales)];
}

export function localizedArticlePath(basePath, locale) {
  const config = ARTICLE_LOCALE_CONFIG[locale] || ARTICLE_LOCALE_CONFIG[DEFAULT_ARTICLE_LOCALE];
  return `${config.prefix}${basePath}`;
}

export function buildArticleLanguageLinks(articleId, basePath) {
  return listPublishedArticleLocales(articleId).map((locale) => ({
    locale,
    label: ARTICLE_LOCALE_CONFIG[locale].label,
    hreflang: ARTICLE_LOCALE_CONFIG[locale].hreflang,
    href: localizedArticlePath(basePath, locale),
  }));
}
